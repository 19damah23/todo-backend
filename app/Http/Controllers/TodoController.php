     * @param  \App\Models\todo  $todo
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $todo = todo::find($id);
        $todo->title = $request->title;
        $todo->description = $request->description;
        $todo->update();
        return response([
            'status' => 200,
            'message' => 'Todo successfully update',
            'data' => $todo
        ]);
    }

